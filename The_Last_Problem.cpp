int arr[2050][2050];
void printPattern(int n)
{
    int k, i, j, p = 1, f;
    for (k = 0; k < n; k++)
    {
        j = k;
        i = 0;
        while (j >= 0)
        {
            arr[i][j] = p;
            p++;
            i = i + 1;
            j = j - 1;
        }
    }

    for (k = 1; k < n; k++)
    {
        i = k;
        j = n - 1;
        f = k;
        while (j >= f)
        {
            arr[i][j] = p;
            p++;
            i = i + 1;
            j = j - 1;
        }
    }
}
