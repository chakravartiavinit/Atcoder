string solve(int a)
{
    if (a <= 399)
    {
        return "gray";
    }
    else if (400 <= a && a <= 799)
    {
        return "brown";
    }
    else if (800 <= a && a <= 1199)
    {
        return "green";
    }
    else if (1200 <= a && a <= 1599)
    {
        return "cyan";
    }
    else if (1600 <= a && a <= 1999)
    {
        return "blue";
    }
    else if (2000 <= a && a <= 2399)
    {
        return "yellow";
    }
    else if (2400 <= a && a <= 2799)
    {
        return "orange";
    }
    else if (2800 <= a && a <= 3199)
    {
        return "red";
    }
    else
    {
        return "none";
    }
}
